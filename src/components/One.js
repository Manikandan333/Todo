import React from 'react';

class One {
    constructor() {

        constructor();
        {
            super();
            this.state = {
                count: 0
            };
        }
        mybutton = () => {
            this.setState(
                {
                    count: this.state.count + 1
                });
        };

        render();
        {
            return;
            {
                <div>
                    <h1>Click down button</h1>
                    <button onclick={this.mybutton}></button>
                </div>;
            }
        }
    }
}